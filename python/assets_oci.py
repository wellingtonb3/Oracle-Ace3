# --- 1. CLEAN DATA FETCHING (2-Decimal Precision Fix) ---
import yfinance as yf
import pandas_ta as ta
import pandas as pd
import oracledb

# The assets you chose: Gold, Silver, and Copper (Bronze)
assets = ["GC=F", "SI=F", "HG=F"]
all_rows = []

print("Consulting market assets and applying 2-decimal rounding...")

for symbol in assets:
    # Fetch 2 months of data
    df = yf.download(symbol, period="2mo", interval="1d", progress=False)

    # Fix for yfinance MultiIndex (The "AttributeError" fix)
    if isinstance(df.columns, pd.MultiIndex):
        df.columns = df.columns.get_level_values(0)

    # Calculate RSI
    df.ta.rsi(length=14, append=True)

    # Remove rows with empty values (The "DPY-4004" fix)
    df = df.dropna(subset=['High', 'Low', 'RSI_14'])

    for index, row in df.iterrows():
        # APPLYING ROUND(x, 2) TO EVERYTHING
        all_rows.append((
            symbol,
            index.to_pydatetime(),
            round(float(row['Open']), 2),
            round(float(row['High']), 2),
            round(float(row['Low']), 2),
            round(float(row['Close']), 2),
            round(float(row['RSI_14']), 2)
        ))

print(f"Extraction Complete! {len(all_rows)} clean rows ready for OCI.")

# Quick Preview of the rounded data
preview_df = pd.DataFrame(all_rows, columns=['Asset', 'Date', 'Open', 'High', 'Low', 'Close', 'RSI'])
print("\nPreview of Clean Data:")
print(preview_df.tail(5))

