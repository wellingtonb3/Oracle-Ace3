        # Prevent ORA-12838: Disabling parallel DML for this session
        cursor.execute("ALTER SESSION DISABLE PARALLEL DML")

        # The SQL Logic: 7 columns, 7 placeholders (:1 to :7)
        sql = """
        MERGE INTO market_assets t
        USING (SELECT :1 as s, :2 as d, :3 as o, :4 as h, :5 as l, :6 as c, :7 as r FROM dual) src
        ON (t.asset_symbol = src.s AND t.trade_date = src.d)
        WHEN NOT MATCHED THEN
            INSERT (asset_symbol, trade_date, open_price, high_price, low_price, close_price, rsi_14)
            VALUES (src.s, src.d, src.o, src.h, src.l, src.c, src.r)
        WHEN MATCHED THEN
            UPDATE SET
                open_price = src.o,
                high_price = src.h,
                low_price = src.l,
                close_price = src.c,
                rsi_14 = src.r
        """

        # Execute the merge using the rounded data from Step 1
        cursor.executemany(sql, all_rows)
